void main() {
  int num1 = 32;
  int num2 = 4;

  // Perform mathematical operations
  print("Sum: ${sum(num1, num2)}"); // Add
  print("Product: ${product(num1, num2)}"); // Multiply
}

// Function to add two numbers
int sum(int a, int b) {
  return a + b;
}

// Function to multiply two numbers
int product(int a, int b) {
  return a * b;
}


